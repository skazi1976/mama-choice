def update_article_seo(filepath, seo_data):
    """Update title, meta description, h1, and keywords for an article."""
    filename = os.path.basename(filepath)

    with open(filepath, 'r', encoding='utf-8') as f:
        html = f.read()

    changes = 0

    # Update <title>
    if 'title' in seo_data:
        html, n = re.subn(r'<title>.*?</title>', f'<title>{seo_data["title"]}</title>', html)
        changes += n

    # Update meta description
    if 'description' in seo_data:
        html, n = re.subn(
            r'<meta name="description" content=".*?">',
            f'<meta name="description" content="{seo_data["description"]}">',
            html
        )
        changes += n

    # Update meta keywords
    if 'keywords' in seo_data:
        html, n = re.subn(
            r'<meta name="keywords" content=".*?">',
            f'<meta name="keywords" content="{seo_data["keywords"]}">',
            html
        )
        changes += n

    # Update og:title
    if 'title' in seo_data:
        og_title = seo_data['title'].replace(' | MamaChoice', '')
        html, n = re.subn(
            r'<meta property="og:title" content=".*?">',
            f'<meta property="og:title" content="{og_title}">',
            html
        )
        changes += n

    # Update og:description
    if 'description' in seo_data:
        html, n = re.subn(
            r'<meta property="og:description" content=".*?">',
            f'<meta property="og:description" content="{seo_data["description"][:200]}">',
            html
        )
        changes += n

    # Update twitter:title
    if 'title' in seo_data:
        tw_title = seo_data['title'].replace(' | MamaChoice', '')
        html, n = re.subn(
            r'<meta name="twitter:title" content=".*?">',
            f'<meta name="twitter:title" content="{tw_title}">',
            html
        )
        changes += n

    # Update twitter:description
    if 'description' in seo_data:
        html, n = re.subn(
            r'<meta name="twitter:description" content=".*?">',
            f'<meta name="twitter:description" content="{seo_data["description"][:200]}">',
            html
        )
        changes += n

    # Update H1
    if 'h1' in seo_data:
        html, n = re.subn(
            r'<h1>.*?</h1>',
            f'<h1>{seo_data["h1"]}</h1>',
            html
        )
        changes += n

    # Update Article schema headline
    if 'title' in seo_data:
        headline = seo_data['title'].replace(' | MamaChoice', '').replace('[', '').replace(']', '')
        html = re.sub(
            r'"headline":"[^"]*"',
            f'"headline":"{headline}"',
            html
        )

    with open(filepath, 'w', encoding='utf-8') as f:
        f.write(html)

    return changes


def main():
    print("=" * 60)
    print("MamaChoice Keyword Optimizer")
    print("=" * 60)

    updated = 0
    skipped = 0

    for filename, seo_data in sorted(ARTICLE_SEO.items()):
        filepath = os.path.join(ARTICLES_DIR, filename)
        if not os.path.exists(filepath):
            print(f"  SKIP (not found): {filename}")
            skipped += 1
            continue

        changes = update_article_seo(filepath, seo_data)
        print(f"  OK: {filename} ({changes} changes)")
        updated += 1

    print(f"\n{'=' * 60}")
    print(f"Done! Updated: {updated}, Skipped: {skipped}")
    print(f"{'=' * 60}")


if __name__ == '__main__':
    main()
